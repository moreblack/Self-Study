public class AssignmentOperatorExample {//#대입연산자#
    public static void main(String[] args) {
        int result = 0;
        result += 10;//10을 더하고
        System.out.println("result =" + result); 
        result -= 5;//5를 빼고
        System.out.println("result=" + result);
        result *= 3;//3을 곱하고
        System.out.println("result=" + result);
        result /= 5;//5를 나누고
        System.out.println("result=" + result);
        result %=3;//3으로나눈 나머지값
        System.out.println("result"+ result);

    }
    
}